    private void hasta_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hasta_silActionPerformed
        int selected_row = hasta_tablosu.getSelectedRow();
        if(selected_row!=-1){
        String ad = (String) model.getValueAt(selected_row, 0);
        baglanti.hasta_sil(ad);
        model.removeRow(selected_row);
        }
        else{
            if(model.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Tablo boş.");
            }
            else{
                JOptionPane.showMessageDialog(this, "Lütfen silinecek bir hasta seçiniz.");
            }
        }
        
        
    }//GEN-LAST:event_hasta_silActionPerformed

    private void hasta_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hasta_guncelleActionPerformed
        int selected_row = hasta_tablosu.getSelectedRow();
        
        if(selected_row==-1){
            if(model.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Tablo boş.");
            }
            else{
                JOptionPane.showMessageDialog(this, "Lütfen güncellenecek bir hasta seçiniz.");
            }
        }
        else{
            String eski_ad = (String)model.getValueAt(selected_row, 0);
            String eski_soyad = (String)model.getValueAt(selected_row, 1);
            String eski_tc = (String)model.getValueAt(selected_row, 2);
            String eski_hastalik = (String)model.getValueAt(selected_row, 3);
            int eski_derece = (int)model.getValueAt(selected_row, 4);
            Ekle_UI ekle = new Ekle_UI(eski_ad, eski_soyad, eski_tc, eski_hastalik, eski_derece);
            ekle.show();
            setVisible(false);
        }
    }//GEN-LAST:event_hasta_guncelleActionPerformed

