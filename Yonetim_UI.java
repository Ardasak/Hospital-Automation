    public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(model);
        hasta_tablosu.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(ara));
    }
    

    public void hastalariGoruntule(){
        model.setRowCount(0);
        ArrayList<hasta> arraylist;
        arraylist = baglanti.hastalariGetir();
        if(arraylist!= null){
            for(hasta hasta : arraylist){
                Object[] eklenecek = {hasta.getAd(),hasta.getSoyad(),hasta.getTc(),hasta.getHastalık(),hasta.getDerece()};
                model.addRow(eklenecek);
            }
        }
    }
